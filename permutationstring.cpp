#include<iostream>
#include<stdio.h>
using namespace std;

void swap(char *x,char *y)
{
	char temp;
	temp=*x;
	*x=*y;
	*y=temp;
}

void permutation(char *a,int i,int n)
{
	int j;
	if(i==4)
		{
			printf("%s",a);
			printf("\n");
		}
	else
	{
		for(j=i;j<=n;j++)
		{
			swap(a+i,a+j);
			permutation(a,i+1,n);
			swap(a+i,a+j);	
		}
		
	}
}

int main()
{
	char a[]="VIVEK";
	permutation(a,0,4);
	return 0;
}

